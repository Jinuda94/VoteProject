package pack01.beans;

public class Person {
	private String name;
	private String se_num;
	private int vote;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSe_num() {
		return se_num;
	}
	public void setSe_num(String se_num) {
		this.se_num = se_num;
	}
	public int getVote() {
		return vote;
	}
	public void setVote(int vote) {
		this.vote = vote;
	}
	
	
}
